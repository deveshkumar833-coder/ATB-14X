package ex_10_For_Loop;

public class Lab109_for_greatervsequal {
    public static void main(String[] args) {
        for (int i = 0; i <=10 ; i++) {  // Print 0 to 10 but run 11 times
            System.out.println(i);

        }
        for (int i = 1; i <=10 ; i++) {  // Print 1 to 10 but run 11 times
            System.out.println(i);}
        for (int i = 0; i <10 ; i++) {  // Print 0 to 9 but run 10 times
            System.out.println(i);}
    }
}
