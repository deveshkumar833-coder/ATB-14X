package ex_10_For_Loop;

public class Lab103_for_IF_LOOP {
    public static void main(String[] args) {
        for (int Dev = 0; Dev < 18; Dev++) {
            if(Dev > 15){
                System.out.println("Get gift from papa");

            }else{
                System.out.println("NO gift");
            }

        }
    }
}
